const Notebook = JSON.parse(notebook);
let currentPage = 1;
const totalPages = Math.ceil(notebook.length / 7);

function showTable(pageNumber) {
  currentPage = pageNumber;
  const startIndex = (currentPage - 1) * 7;
  const endIndex = Math.min(startIndex + 7, notebook.length);
  const tableBody = document.getElementById("display");

  tableBody.innerHTML = ""; // Clear table content

  for (let i = startIndex; i < endIndex; i++) {
    if (Notebook[i]) {
      tableBody.innerHTML += `
        <tr>
          <td><img src="/public/img/DATA_IMG/${Notebook[i].Image}" alt="" style="margin: 0 auto;"></td>
          <td>${Notebook[i].Name}</td>
          <td>${Notebook[i].Borrower}</td>
          <td>${Notebook[i].Startdate}</td>
          <td>${Notebook[i].Lender}</td>
          <td>${Notebook[i].Enddate}</td>
          <td>${Notebook[i].Staff}</td>
        </tr>
      `;
    }
  }
}

// Show initial data for the first page
showTable(1);

// Button Event Listeners
document.getElementById("next").addEventListener("click", () => {
  if (currentPage < totalPages) {
    showTable(currentPage + 1);
  }
});

document.getElementById("Back").addEventListener("click", () => {
  if (currentPage > 1) {
    showTable(currentPage - 1);
  }
});
